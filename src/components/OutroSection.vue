<script>
import { ref, onMounted, onBeforeUnmount, watch } from 'vue';
import { gsap } from 'gsap';
import { ScrollTrigger } from 'gsap/ScrollTrigger';

export default {
  name: 'OutroSection',
  props: {
    // Add prop to receive slider state from parent component
    dogDisplayMode: {
      type: String,
      default: 'maximum', // Default to maximum view
      validator: (value) => ['minimal', 'medium', 'maximum'].includes(value)
    }
  },
  setup(props) {
    const wrapperConclusion = ref(null);
    const card1 = ref(null);
    const card2 = ref(null);
    const card3 = ref(null);
    const card4 = ref(null);
    const card5 = ref(null);
    const cardRoselle = ref(null); // Add a new ref for Roselle's card
    const card6 = ref(null); // Add a new ref for the card-6
    const navRef = ref(null); // Add ref for nav element
    const cardRiley = ref(null);
    const cardSpike = ref(null);
    const cardDaisy = ref(null); 
    const cardDuke = ref(null);

    // Replace static sliderState with a reactive ref that watches the prop
    const sliderState = ref(props.dogDisplayMode);
    
    // Watch for changes to the dogDisplayMode prop
    watch(() => props.dogDisplayMode, (newMode) => {
      sliderState.value = newMode;
    });

    // Store ScrollTrigger instances for cleanup
    const scrollTriggers = [];

    onMounted(() => {
      // Register ScrollTrigger plugin
      gsap.registerPlugin(ScrollTrigger);
      
      // Ensure elements are available
      if (!wrapperConclusion.value) return;

      // Make nav fixed when the section is in view
      const navTrigger = ScrollTrigger.create({
        trigger: ".gsap-section",
        start: "top top", // Only start when OutroSection is at the top
        end: "bottom bottom",
        onEnter: () => {
          gsap.to(".gsap-section nav", {
            position: "fixed",
            top: "20px",
            opacity: 1,
            duration: 0.3
          });
        },
        onLeave: () => {
          gsap.to(".gsap-section nav", {
            opacity: 0,
            duration: 0.3
          });
        },
        onEnterBack: () => {
          gsap.to(".gsap-section nav", {
            position: "fixed",
            top: "20px",
            opacity: 1,
            duration: 0.3
          });
        },
        onLeaveBack: () => {
          gsap.to(".gsap-section nav", {
            opacity: 0,
            duration: 0.3
          });
        }
      });
      scrollTriggers.push(navTrigger);

      // Your existing code continues...
      const cards = [
        { ref: card1, id: "#card-1", endTranslateX: -500, rotate: 10 },
        { ref: card2, id: "#card-2", endTranslateX: -1000, rotate: -30 },
        { ref: cardRiley, id: "#card-riley", endTranslateX: -1300, rotate: 15 },
        { ref: card3, id: "#card-3", endTranslateX: -2000, rotate: 45 },
        { ref: cardRoselle, id: "#card-roselle", endTranslateX: -1200, rotate: 25 },
        { ref: card4, id: "#card-4", endTranslateX: -1500, rotate: -30 },
        { ref: cardSpike, id: "#card-spike", endTranslateX: -1700, rotate: 20 },
        { ref: cardDaisy, id: "#card-daisy", endTranslateX: -2100, rotate: -25 },
        { ref: card5, id: "#card-5", endTranslateX: -2500, rotate: 20 },
        { ref: card6, id: "#card-6", endTranslateX: -1800, rotate: -15 },
        { ref: cardDuke, id: "#card-duke", endTranslateX: -2200, rotate: 30 }
      ];

      // Main wrapper animation
      const mainTrigger = ScrollTrigger.create({
        trigger: wrapperConclusion.value,
        start: "top top",
        end: "+=900vh", // How long the pinning lasts
        scrub: 1,
        pin: true,
        anticipatePin: 1,
        onUpdate: (self) => {
          gsap.to(wrapperConclusion.value, {
            x: `${-600 * self.progress}vw`,
            duration: 0.5,
            ease: "power3.out",
          });
        },
      });
      scrollTriggers.push(mainTrigger);

      // Individual card animations
      cards.forEach((card) => {
        if (!card.ref.value) return;

        // Create a GSAP timeline for each card to ensure synchronized animations
        const tl = gsap.timeline({
          scrollTrigger: {
            trigger: card.ref.value,
            start: "top top",
            end: "+=1200vh",
            scrub: 1,
            onUpdate: (self) => {
              // Apply animation to the card element itself
              gsap.to(card.ref.value, {
                x: `${card.endTranslateX * self.progress}px`,
                rotate: `${card.rotate * self.progress * 2}deg`,
                duration: 0.5,
                ease: "power3.out",
                // Force 3D transforms to ensure proper stacking and prevent layout issues
                force3D: true,
                // Ensure transforms are applied to the entire card as a unit
                transformOrigin: "center center"
              });
            }
          }
        });
        
        // Make sure internal elements maintain their positions relative to the card
        const cardContent = card.ref.value.querySelector('.card-content');
        const cardImage = card.ref.value.querySelector('.card-image');
        
        if (cardContent && cardImage) {
          // Set initial positions for internal elements to prevent movement during animation
          gsap.set([cardContent, cardImage], {
            position: "relative",
            xPercent: 0,
            yPercent: 0
          });
        }
        
        scrollTriggers.push(tl.scrollTrigger);
      });

      // Outro text and slider images animation
      const outroTrigger = ScrollTrigger.create({
        trigger: ".gsap-section .outro",
        start: "top 80%",
        toggleActions: "play none none reverse",
        onEnter: () => {
          // Animate text
          gsap.fromTo(".gsap-section .outro h1",
            { opacity: 0, y: 30 },
            { opacity: 1, y: 0, duration: 1, ease: "power2.out" }
          );
          
          // Animate slider images
          gsap.fromTo(".gsap-section .outro .dog-image",
            { opacity: 0, scale: 0.8 },
            { 
              opacity: 1, 
              scale: 1, 
              duration: 0.8, 
              stagger: 0.1,
              ease: "back.out(1.7)" 
            }
          );
          
          // Animate decorative elements
          gsap.fromTo(".gsap-section .outro .decoration",
            { opacity: 0, scale: 0.5, rotation: -30 },
            { 
              opacity: 1, 
              scale: 1, 
              rotation: 0,
              duration: 0.6, 
              stagger: 0.15,
              ease: "elastic.out(1, 0.3)" 
            }
          );
        }
      });
      scrollTriggers.push(outroTrigger);
      
      // Optional: Add logic to switch between slider states based on scroll position
      // REMOVE OR COMMENT OUT THIS ENTIRE BLOCK:
      /*
      const sliderStateTrigger = ScrollTrigger.create({
        trigger: ".gsap-section .outro",
        start: "top 50%",
        end: "bottom 20%",
        onUpdate: (self) => {
          // Change state based on scroll progress
          if (self.progress < 0.3) {
            sliderState.value = 'medium';
          } else {
            sliderState.value = 'maximum';
          }
        }
      });
      scrollTriggers.push(sliderStateTrigger);
      */
    });

    onBeforeUnmount(() => {
      // Kill all ScrollTrigger instances associated with this component
      scrollTriggers.forEach(trigger => trigger.kill());
      console.log("OutroSection ScrollTriggers killed");
    });

    return {
      wrapperConclusion,
      card1,
      card2,
      cardRiley,
      card3,
      cardRoselle,
      card4,
      cardSpike,
      cardDaisy,
      card5,
      card6,
      cardDuke,
      navRef,
      sliderState
    };
  },
};
</script>

